	protected function validate() {
		if (!$this->user->hasPermission('modify', 'module/customer_toggle')) {
			$this->error['warning'] = $this->language->get('error_permission');
		}


		return !$this->error;
	}
	
	public function updateToggle(){
		$json = array();
		$this->load->language('module/customer_toggle');
		$this->load->model('module/customer_toggle');
		
		if(isset($this->request->get['toggle']) && isset($this->request->get['customer_id'])){
			$toggle = $this->request->get['toggle'];
			$customer_id = $this->request->get['customer_id'];
			$this->model_module_customer_toggle->updateToggle($customer_id,$toggle);
			$json['success'] = $this->language->get('update_success');
		}else{
			$json['error'] = $this->language->get('error_toggle');
		}
		
		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode($json));
	}
	
	public function install(){
		$this->load->model('module/customer_toggle');
		$this->model_module_customer_toggle->install();
	}
